class Store:
    def __init__(self, название, адрес):
        self.название = название
        self.адрес = адрес
        self.товары = {}

    def добавить_товар(self, название_товара, цена):
        self.товары[название_товара] = цена

    def удалить_товар(self, название_товара):
        if название_товара in self.товары:
            del self.товары[название_товара]

    def получить_цену(self, название_товара):
        return self.товары.get(название_товара)

    def обновить_цену(self, название_товара, новая_цена):
        if название_товара in self.товары:
            self.товары[название_товара] = новая_цена

# Создаем несколько объектов класса Store
магазин_1 = Store("Свежие Фрукты", "ул. Ленина, 123")
магазин_1.добавить_товар("яблоки", 50)
магазин_1.добавить_товар("бананы", 75)

магазин_2 = Store("Овощной Восторг", "ул. Кленовая, 456")
магазин_2.добавить_товар("морковь", 60)
магазин_2.добавить_товар("брокколи", 80)

магазин_3 = Store("Пекарня Блаженство", "ул. Дубовая, 789")
магазин_3.добавить_товар("хлеб", 55)
магазин_3.добавить_товар("торт", 90)

магазины = [магазин_1, магазин_2, магазин_3]

def выбрать_магазин():
    print("Выберите магазин:")
    for i, магазин in enumerate(магазины):
        print(f"{i + 1}. {магазин.название} - {магазин.адрес}")
    while True:
        try:
            выбор = int(input("Введите номер магазина: ")) - 1
            if 0 <= выбор < len(магазины):
                return магазины[выбор]
            else:
                print("Некорректный номер, попробуйте снова.")
        except ValueError:
            print("Пожалуйста, введите числовое значение.")

def main():
    магазин = выбрать_магазин()

    while True:
        print(f"\nТекущие товары в {магазин.название}: {магазин.товары}")
        print("Варианты:")
        print("1. Добавить товар")
        print("2. Удалить товар")
        print("3. Обновить цену товара")
        print("4. Узнать цену товара")
        print("5. Выйти")
        try:
            вариант = int(input("Выберите вариант: "))
        except ValueError:
            print("Пожалуйста, введите числовое значение.")
            continue

        if вариант == 1:
            название_товара = input("Введите название товара: ")
            try:
                цена = float(input("Введите цену товара: "))
                магазин.добавить_товар(название_товара, цена)
            except ValueError:
                print("Цена должна быть числом.")
        elif вариант == 2:
            название_товара = input("Введите название товара для удаления: ")
            магазин.удалить_товар(название_товара)
        elif вариант == 3:
            название_товара = input("Введите название товара для обновления: ")
            try:
                новая_цена = float(input("Введите новую цену: "))
                магазин.обновить_цену(название_товара, новая_цена)
            except ValueError:
                print("Цена должна быть числом.")
        elif вариант == 4:
            название_товара = input("Введите название товара, чтобы узнать цену: ")
            цена = магазин.получить_цену(название_товара)
            if цена is not None:
                print(f"Цена {название_товара}: {цена}")
            else:
                print(f"{название_товара} не найден в магазине.")
        elif вариант == 5:
            break
        else:
            print("Неверный вариант. Пожалуйста, попробуйте снова.")

if __name__ == "__main__":
    main()